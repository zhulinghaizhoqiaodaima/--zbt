package com.ly.model;

public class Ivt {
    private int Ivid;
    private String kind;
    private String imessage;
    private int inum;
    private String unit;
    private int Ppid;

    public int getIvid() {
        return Ivid;
    }

    public void setIvid(int ivid) {
        Ivid = ivid;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getImessage() {
        return imessage;
    }

    public void setImessage(String imessage) {
        this.imessage = imessage;
    }

    public int getInum() {
        return inum;
    }

    public void setInum(int inum) {
        this.inum = inum;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public int getPpid() {
        return Ppid;
    }

    public void setPpid(int ppid) {
        Ppid = ppid;
    }
}
